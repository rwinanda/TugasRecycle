package com.example.recyclenanda;

public class Klub {

    private String namaklub;
    private String desklub;
    private int logo;

    public String getNamaklub() {
        return namaklub;
    }

    public void setNamaklub(String namaklub) {
        this.namaklub = namaklub;
    }

    public String getDesklub() {
        return desklub;
    }

    public void setDesklub(String desklub) {
        this.desklub = desklub;
    }

    public int getLogo() {
        return logo;
    }

    public void setLogo(int logo) {
        this.logo = logo;
    }
}
